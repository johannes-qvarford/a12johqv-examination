package d2.core;

import gatech.mmpm.util.XMLWriter;

/**
 *
 * @author santi
 */
public class D2Module {
    // all D2 modules have the functionality of writing a reasoning trace to monitor D2's behavior:
    public XMLWriter reasoningTraceWriter = null;

    public void D2Module() {

    }

    public void D2Module(XMLWriter w) {
        reasoningTraceWriter = w;
    }

    public void setReasoningTraceWriter(XMLWriter w) {
        reasoningTraceWriter = w;
    }
}
